num = 5
print(num)  # Output: 5
num = num + 2 
print(num)  # Output: 7
num = num // 3 * 6
print(num)  # Output: 12
print(7 + 15 % 4)  # Output: 10
num = 24 // 3 // 4
print(num)  # Output: 2

# Explanation:

# First, we set num to 5
# Then, we add 2 to num, making it 7
# Next, num // 3 is integer division, giving 2, then multiplied by 6, resulting in 12
# 15 % 4 is the remainder of 15 divided by 4, which is 3, plus 7 equals 10
# 24 // 3 gives 8, then 8 // 4 results in 2