# Ask the age from the user
age = int(input("Enter your age: "))

# Check if the person is old enough to vote
if age >= 18:
    print("You can vote!")
else:
    print("You cannot vote yet.")


# If the age entered is below 18 then the output will be You can vote
# If the age entered is above 18 then the output will be you cannot vote yet. 